function make_stack(element, i) {
    let stickyDiv = document.getElementById(element);
    let stickyValue = 55 + 10 * i; // will stick on the top of the div
    stickyDiv.style.top = stickyValue + 'px';
    document.getElementById('scroll').addEventListener('scroll', function () {
        let distance = stickyDiv.getBoundingClientRect().top;
    });
}

